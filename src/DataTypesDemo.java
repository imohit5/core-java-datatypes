
public class DataTypesDemo {

	byte a =-50; //-128 to +127            //1 bytes
	short b= 150; // -32768 to +32767      //2 bytes
	int c = 10000; //-2^31 to 2^31-1       //4 bytes
	long d = 100000; //-2^63 to 2^63-1      //8 bytes
	char e = 'A'; // '\u0000' (or 0) to '\uffff' (or 65,535 inclusive)       //2 bytes
	
	float f = 1.23f; // -3.40282347E+38F to +3.40282347E+38F      //4 bytes
	double g = 12345.6789; //-1.79769313486231570E+308 to +1.79769313486231570E+308      //8 bytes
	
	boolean h = true; // true or false
	
	
	
}
